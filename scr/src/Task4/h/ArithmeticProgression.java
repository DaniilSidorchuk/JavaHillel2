package Task4.h;

public class ArithmeticProgression {

    public static void main(String[] args) {
        progression();
    }
    
    public static void progression (){
        int x = 10;
        for (int i = 0; i < 1; ) {
            System.out.println(x);
            x+=2;
        }
        
    }    
    
}
